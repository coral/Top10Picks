<div id="placecardinfo">
Färgfabriken har fortsatt att markera sin ställning som 'samtidslaboratorium' där både utställningar och seminarier handlar om viktiga dagsaktuella frågor. Bland annat pågår forskningsarbete med att definiera idéer, projekt och strategier inom urban utveckling, jämställdhet, energifrågor och miljö. I samtliga fall handlar det om samarbeten och nätverksbyggen med en rad instanser på både lokal, regional och global nivå. Målet har varit att se vilken roll Stockholm och Sverige skulle kunna spela i ett större globalt perspektiv. Färgfabriken har också etablerat sig som scen för en yngre målgrupp både som diskussion och debattskapare samt genom samarbete med högskolor.
